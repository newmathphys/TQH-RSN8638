"""Топологический супер-дрейф: информационный варп RSN-8638.
Запуск: python3 tests/test_warp_drift.py
"""
import numpy as np
import matplotlib
matplotlib.use('Agg')
import matplotlib.pyplot as plt
import sys, os
sys.path.insert(0, os.path.dirname(os.path.dirname(__file__)))

GAMMA_1 = 14.1347251417
k_m = GAMMA_1 / (16 * 137.035999084)  # 0.006447 — ЕДИНЫЙ
N = 8638
c = 299792458

SAVE = 'docs/figures_warp'
os.makedirs(SAVE, exist_ok=True)

print("=" * 75)
print("ТОПОЛОГИЧЕСКИЙ СУПЕР-ДРЕЙФ: ИНФОРМАЦИОННЫЙ ВАРП RSN-8638")
print("=" * 75)
print(f"Единый k = {k_m:.6f}")
print(f"N = {N}")

# ═════════════════════════════════════════════════════
# ТЕСТ: ВАРП-ДРЕЙФ
# ═════════════════════════════════════════════════════
print(f"\n{'='*70}")
print("[1] ПРОФИЛЬ ДЕФОРМАЦИИ РЕШЁТКИ")
print(f"{'='*70}")

x = np.linspace(-10, 10, 1000)
warp = N / (1 + np.exp(-(x + 3)))
warp -= N / (1 + np.exp(-(x - 3)))
vacuum = N + warp * 0.95

F_warp = -np.gradient(vacuum, x)
v_drift = np.abs(F_warp) / (N * k_m)

print(f"  Сжатие перед кораблём: {np.max(vacuum):.0f} бит")
print(f"  Разрежение позади: {np.min(vacuum):.0f} бит")
print(f"  Пиковая скорость: {np.max(v_drift):.2f}c")

if np.max(v_drift) > 1:
    print(f"  🚀 Сверхсветовой дрейф возможен")
else:
    print(f"  ❌ Скорость < c")

# ═════════════════════════════════════════════════════
# ТЕСТ 2: ЭНЕРГИЯ ВАРПА
# ═════════════════════════════════════════════════════
print(f"\n{'='*70}")
print("[2] ЭНЕРГЕТИКА — сколько нужно энергии")
print(f"{'='*70}")

# Энергия на перезапись одного бита: E_bit = k_B·T·ln(2)
T_vac = 300  # K (из предела Ландауэра)
E_bit = 1.38e-23 * T_vac * np.log(2)  # Дж

# Полная энергия варп-пузыря
N_nodes = int(np.trapezoid(np.abs(warp), x))  # интеграл перегрузки
E_total = N_nodes * E_bit
E_total_kg = E_total / (3e8)**2  # масса-энергия

print(f"  Энергия на бит: {E_bit:.2e} Дж")
print(f"  Число перегруженных узлов: {N_nodes}")
print(f"  Полная энергия: {E_total:.1f} Дж")
print(f"  Эквивалент массы: {E_total_kg:.2e} кг")
print(f"  {'✅ Реалистично' if E_total_kg < 1e6 else '❌ Астрономически много'}")

# ═════════════════════════════════════════════════════
# ТЕСТ 3: ВАКУУМ КАК ЖЁСТКИЙ ДИСК
# ═════════════════════════════════════════════════════
print(f"\n{'='*70}")
print("[3] ВАКУУМ КАК ЖЁСТКИЙ ДИСК — свободная память")
print(f"{'='*70}")

# Сколько бит занято частицей
# Масса протона: n=1166 → M = 938 MeV
# Информация: S ≈ ln(числа состояний) ≈ n (число шагов)
S_proton = 1166  # бит
S_free = N - S_proton

print(f"  Ёмкость ячейки: N = {N} бит")
print(f"  Занято протоном: ~{S_proton} бит")
print(f"  Свободно: ~{S_free} бит ({100*S_free/N:.0f}%)")
print(f"  Плотность записи: 1 бит/({N**2}·l_P²) ≈ 10^{np.log10(N**2):.1f} бит/м²")

# ═════════════════════════════════════════════════════
# ТЕСТ 4: НЕЛОКАЛЬНОСТЬ (Мгновенная связь)
# ═════════════════════════════════════════════════════
print(f"\n{'='*70}")
print("[4] НЕЛОКАЛЬНОСТЬ — кротовые норы на ζ(s)")
print(f"{'='*70}")

gamma_1 = 14.1347
# Два узла, связанных через γ₁
delta_n = int(gamma_1)

# Масса в каждом узле
M_node = 0.511e-3 * np.exp(k_m * 1000)  # GeV
# Энергия связи
E_coupling = 0.511e-3 * np.exp(k_m * delta_n)
# Скорость передачи сигнала (мгновенная для связанных узлов)
print(f"  Связь через γ₁={gamma_1:.2f}: Δn = {delta_n}")
print(f"  Энергия связи: E = {E_coupling:.4e} GeV")
print(f"  {'✅ Мгновенная нелокальность (кротовая нора)' if E_coupling > 0 else '❌'}")
print(f"  Пространство-время = информационный код Римана")

# ═════════════════════════════════════════════════════
# ГРАФИКИ
# ═════════════════════════════════════════════════════
print(f"\n{'─'*70}")
print("ГРАФИКИ")
print(f"{'─'*70}")

fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(15, 5))

# График 1: Варп-профиль
ax1.plot(x, vacuum, 'darkblue', lw=2.5, label='Битовое поле')
ax1.axhline(N, color='gray', ls='--', label=f'Вакуум ({N} бит)')
ax1.fill_between(x, vacuum, N, where=(vacuum>N), color='red', alpha=0.3, label='Сжатие')
ax1.fill_between(x, vacuum, N, where=(vacuum<N), color='green', alpha=0.3, label='Разрежение')
ax1.scatter([0], [N], color='purple', marker='*', s=150, label='Корабль')
ax1.set_title('Профиль информационного варп-пузыря')
ax1.set_xlabel('x'); ax1.set_ylabel('бит')
ax1.legend(fontsize=8); ax1.grid(alpha=0.3)

# График 2: Сила и скорость
ax2.plot(x, F_warp, 'darkmagenta', lw=2.5, label='Сила дрейфа')
ax2.plot(x, v_drift, 'orange', ls=':', label='v/c')
ax2.set_title('Энтропийная сила и скорость')
ax2.set_xlabel('x'); ax2.set_ylabel('Амплитуда')
ax2.legend(fontsize=8); ax2.grid(alpha=0.3)

plt.tight_layout()
plt.savefig(f'{SAVE}/warp_drift.png', dpi=150)
print(f"  ✅ График: {SAVE}/warp_drift.png")

